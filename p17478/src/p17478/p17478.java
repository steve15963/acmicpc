package p17478;

import java.util.Scanner;

public class p17478 {
	
	public static StringBuilder underBar(int time) {
		StringBuilder sb = new StringBuilder();
		for(int i = 0;i < time;i++) {
			sb.append("____");
		}
		return sb;
	}
	
	public static void recall(StringBuilder sb, int time, int round) {
		StringBuilder under = underBar(time);
		sb.append(under).append("\"재귀함수가 뭔가요?\"\n");
		if(time == round)
			sb.append(under).append("\"재귀함수는 자기 자신을 호출하는 함수라네\"\n");
		else {
			sb.append(under).append("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n");
			sb.append(under).append("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n");
			sb.append(under).append("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n");
			recall(sb, time+1, round);
		}
		sb.append(under).append("라고 답변하였지.\n");
	}
	
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		StringBuilder sb = new StringBuilder().append("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n");
		recall(sb, 0, sc.nextInt());
		System.out.println(sb.toString());
		sc.close();
	}
}
